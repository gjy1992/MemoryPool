#include <time.h>
#include <Windows.h>

inline double getMS()
{
	LARGE_INTEGER f;
	QueryPerformanceFrequency(&f);
	LARGE_INTEGER t;
	QueryPerformanceCounter(&t);
	return (double)t.QuadPart / (double)f.QuadPart * 1000;
}